package com.revature.app;

public class Example {

	public static void main(String[] args) {
		
		//Casting to a short
		int i = 200;
		short s = (short)i;
		System.out.println(s);
		
		//Casting to a double
		double d = (double)i;
		System.out.println(d);
		
		//Casting int to a byte
		byte b = (byte)i;
		System.out.println(b);

	}

}
