package com.revature.model;

public class GrandParent {
	
	String firstName;
	String lastName;
	int age;
	String birthPlace;
	
	
	public GrandParent(String firstName, String lastName, int age, String birthPlace) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.age = age;
		this.birthPlace = birthPlace;
	}
	
	

}
